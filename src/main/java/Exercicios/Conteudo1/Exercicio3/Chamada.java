    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtmChamada = (DefaultTableModel) jTable1.getModel();
        
        if(jCheckBox1.isSelected()){
            Marcado = "Presente";
        }else{
            Marcado = "Falta";
        }
        
        Object[] dados = {jTextField1.getText(), 
                          jTextField2.getText(),
                          Marcado
                          
        };
        dtmChamada.addRow(dados);
        
        jCheckBox1.setSelected(false);
        jTextField1.setText("");
        jTextField2.setText("");
        Marcado = "";
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        jTable1.setValueAt(jTextField1.getText(), jTable1.getSelectedRow(), 0);
        jTable1.setValueAt(jTextField2.getText(), jTable1.getSelectedRow(), 1);
        
        if(jCheckBox1.isSelected()){
            Marcado = "Presente";
        }else{
            Marcado = "Falta";
        }
        jTable1.setValueAt(Marcado, jTable1.getSelectedRow(), 2);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jTextField1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        jTextField2.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
        if(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString() == "Presente"){
            jCheckBox1.setSelected(true);
        }else{
            jCheckBox1.setSelected(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked

