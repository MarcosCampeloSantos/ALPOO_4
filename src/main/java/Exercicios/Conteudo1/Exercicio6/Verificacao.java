    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtmChamada = (DefaultTableModel) jTable1.getModel();
        
        if(jCheckBox1.isSelected()){
            Marcado = "Sim";
        }else{
            Marcado = "NÃ£o";
        }
        
        Object[] dados = {jTextField1.getText(), 
                          jTextField2.getText(),
                          jTextField3.getText(),
                          jTextField4.getText(),
                          Marcado
                          
        };
        dtmChamada.addRow(dados);
        
        jCheckBox1.setSelected(false);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        Marcado = "";
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        jTable1.setValueAt(jTextField1.getText(), jTable1.getSelectedRow(), 0);
        jTable1.setValueAt(jTextField2.getText(), jTable1.getSelectedRow(), 1);
        jTable1.setValueAt(jTextField3.getText(), jTable1.getSelectedRow(), 2);
        jTable1.setValueAt(jTextField4.getText(), jTable1.getSelectedRow(), 3);
        
        if(jCheckBox1.isSelected()){
            Marcado = "Sim";
        }else{
            Marcado = "NÃ£o";
        }
        jTable1.setValueAt(Marcado, jTable1.getSelectedRow(), 4);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jTextField1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        jTextField2.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
        jTextField3.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
        jTextField4.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
        if(jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString() == "Presente"){
            jCheckBox1.setSelected(true);
        }else{
            jCheckBox1.setSelected(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtmVerificacao = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRow()!= -1){
            dtmVerificacao.removeRow(jTable1.getSelectedRow());
        }
    }//GEN-LAST:event_jButton3MouseClicked

